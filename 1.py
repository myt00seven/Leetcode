# It's strange that with python, O(n^2) algorithm runs faster than and O(n) algorithm.

class Solution(object):
    def twoSum(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: List[int]
        """
        for i in range(len(nums)):
            for j in range(len(nums)):
                if i<j:
                    if nums[i]+nums[j]==target:
                        return 
                
